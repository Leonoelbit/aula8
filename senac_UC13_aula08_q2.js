function definirDiasemana(Dia) {
    if (Dia = 1) {
        return "Segunda"
    } else if (Dia = 2) {
        return "Terça"
    } else if (Dia = 3) {
        return "Quarta"
    } else if (Dia = 4) {
        return "Quinta"
    } else if (Dia = 5) {
        return "Sexta"
    } else if (Dia = 7) {
        return "Sábado"
    } else return "Número INválido"





}